public class Basic 
{
    public static void main(String args[])
    {
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(~5);
        System.out.println(5 ^ 6);
        System.out.println(5<<4);
        System.out.println(5<<2);
        System.out.println(5>>2);
        System.out.println(100>>2);
        System.out.println(6>>2);

        System.out.println((~0)+ " " +(~1)+ " " +(~5)+ " " +(~6));
    }
}